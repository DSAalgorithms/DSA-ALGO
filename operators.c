#include <stdio.h> 

// Arithmetic operator
int main(int argc, char const *argv[])
{
    int a,b;
    a = 20;
    b = 5;

    printf("a+b=%d\n", a+b);
    printf("a-b=%d\n", a-b);
    printf("a*b=%d\n", a*b);
    printf("a/b=%d\n", a/b);

    return 0;
}


